class MockProvider:
    """Simulates an LLM provider without making any real API calls."""

    def __init__(
        self,
        api_key: str = "",
        default_model: str = "mock/default",
        fallback_model: str = "mock/fast",
        base_url: str | None = None,
        provider_name: str = "mock",
        latency_ms: int = 500,
        error_rate: float = 0.0,
        response_prefix: str = "",
    ):
        self._default_model = default_model
        self._fallback_model = fallback_model
        self._provider_name = provider_name
        self._latency_ms = latency_ms
        self._error_rate = max(0.0, min(1.0, error_rate))
        self._response_prefix = response_prefix

    @property
    def name(self) -> str:
        return self._provider_name

    def _get_model(self, model: str | None) -> str:
        return model or self._default_model

    def _maybe_raise(self) -> None:
        """Raise a simulated error if error_rate > 0."""
        if self._error_rate > 0.0 and random.random() < self._error_rate:
            raise ProviderError(
                f"Simulated provider error (error_rate={self._error_rate:.0%})",
                provider=self._provider_name,
            )

    async def complete(
        self,
        messages: list[Message],
        model: str | None = None,
        temperature: float = 1.0,
        max_tokens: int = 4096,
        system_prompt: str | None = None,
        **kwargs: Any,
    ) -> CompletionResponse:
        model_name = self._get_model(model)
        effective_latency = _model_latency(model_name, self._latency_ms)
        start = time.monotonic()

        self._maybe_raise()
        await asyncio.sleep(effective_latency / 1000.0)

        content = _pick_response(messages, self._response_prefix)
        elapsed = int((time.monotonic() - start) * 1000)

        # Estimate realistic token counts
        input_text = " ".join(m.content for m in messages)
        if system_prompt:
            input_text = system_prompt + " " + input_text
        in_tokens = self.estimate_tokens(input_text)
        out_tokens = self.estimate_tokens(content)

        return CompletionResponse(
            content=content,
            model=model_name,
            provider=self._provider_name,
            usage=Usage(
                input_tokens=in_tokens,
                output_tokens=out_tokens,
                total_tokens=in_tokens + out_tokens,
            ),
            cost_usd=Decimal("0"),
            latency_ms=elapsed,
            finish_reason=FinishReason.STOP,
        )

    async def stream(
        self,
        messages: list[Message],
        model: str | None = None,
        temperature: float = 1.0,
        max_tokens: int = 4096,
        system_prompt: str | None = None,
        **kwargs: Any,
    ) -> AsyncIterator[StreamChunk]:
        model_name = self._get_model(model)
        effective_latency = _model_latency(model_name, self._latency_ms)

        self._maybe_raise()

        # Initial delay simulates network round-trip
        await asyncio.sleep(effective_latency / 1000.0 * 0.3)

        content = _pick_response(messages, self._response_prefix)
        words = content.split(" ")
        accumulated = ""

        input_text = " ".join(m.content for m in messages)
        if system_prompt:
            input_text = system_prompt + " " + input_text
        in_tokens = self.estimate_tokens(input_text)

        # Yield word-by-word with small inter-word delay
        inter_word_delay = max(0.01, (effective_latency / 1000.0 * 0.7) / max(len(words), 1))

        for i, word in enumerate(words):
            delta = word + (" " if i < len(words) - 1 else "")
            accumulated += delta
            is_final = i == len(words) - 1

            usage = None
            cost = None
            if is_final:
                out_tokens = self.estimate_tokens(accumulated)
                usage = Usage(
                    input_tokens=in_tokens,
                    output_tokens=out_tokens,
                    total_tokens=in_tokens + out_tokens,
                )
                cost = Decimal("0")

            yield StreamChunk(
                delta=delta,
                is_final=is_final,
                accumulated_content=accumulated,
                model=model_name,
                provider=self._provider_name,
                usage=usage,
                cost_usd=cost,
                finish_reason=FinishReason.STOP if is_final else None,
            )

            if not is_final:
                await asyncio.sleep(inter_word_delay)

    async def list_models(self) -> list[ModelInfo]:
        return list(_MODELS)

    async def health_check(self) -> HealthStatus:
        start = time.monotonic()
        await asyncio.sleep(self._latency_ms / 1000.0 * 0.1)
        elapsed = int((time.monotonic() - start) * 1000)
        return HealthStatus(
            provider=self._provider_name,
            healthy=True,
            latency_ms=elapsed,
            models_available=len(_MODELS),
        )

    def estimate_tokens(self, text: str) -> int:
        # ~4 chars per token is a reasonable approximation
        return max(1, len(text) // 4)
