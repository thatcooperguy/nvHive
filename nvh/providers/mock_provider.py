def _pick_response(messages: list[Message], prefix: str) -> str:
    """Choose a canned response based on the last user message content."""
    last_user = ""
    for msg in reversed(messages):
        if msg.role == "user":
            last_user = msg.content.lower()
            break

    # Detect category from keywords
    code_keywords = {"code", "function", "def ", "class ", "implement", "write a", "script", "program", "algorithm"}
    math_keywords = {"calculate", "compute", "solve", "equation", "integral", "derivative", "sum", "product", "math", "formula", "="}

    body: str
    if any(kw in last_user for kw in code_keywords):
        body = _CODE_TEMPLATE
    elif any(kw in last_user for kw in math_keywords):
        body = _MATH_TEMPLATE
    else:
        # Extract a short topic from the query (first 60 chars)
        topic = last_user[:60].strip() or "your query"
        body = _LOREM_TEMPLATE.format(topic=topic)

    return (prefix + body) if prefix else body


def _model_latency(model: str, base_ms: int) -> int:
    """Adjust latency based on model variant."""
    if "fast" in model:
        return max(50, base_ms // 5)
    if "slow" in model:
        return base_ms * 4
    return base_ms


